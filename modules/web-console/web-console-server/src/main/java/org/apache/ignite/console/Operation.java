package org.apache.ignite.console;

public class Operation {
    private String id;
    private long a;
    private long b;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public long getA() {
        return a;
    }

    public void setA(long a) {
        this.a = a;
    }

    public long getB() {
        return b;
    }

    public void setB(long b) {
        this.b = b;
    }
}
